    private void bsaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bsaveActionPerformed
        // TODO add your handling code here:
        boolean kondisi = thgjual.getText().equals("") || tnama.getText().equals("");
        if ( kondisi ) {
            JOptionPane.showMessageDialog(null, "Data tidak boleh kosong");
        } else {
            String sqlQuery = "INSERT INTO obat values(?,?,?,?,?,?,?)";
            try {
                PreparedStatement stat = conn.prepareStatement(sqlQuery);

                stat.setString(1, tkode.getText());
                stat.setString(2, tnama.getText());
                stat.setString(3, tjenis.getSelectedItem().toString());
                stat.setString(4, tsatuan.getText());
                stat.setString(5, thgbeli.getText());
                stat.setString(6, thgjual.getText());
                stat.setString(7, tstok.getText());
            
            
            stat.executeUpdate();
            JOptionPane.showMessageDialog(null, "Data Berhasil Disimpan");
            kosong();
            thgjual.requestFocus();
            datatable();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Data Gagal Disimpan"+e);
        }
        }
    }//GEN-LAST:event_bsaveActionPerformed

    private void beditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_beditActionPerformed
        // TODO add your handling code here:
        String sqlQuery = "UPDATE obat SET "
                + "nama = ?, jenis = ?, satuan = ?, hgbeli = ?, hgjual = ?, stok = ? WHERE kode = ?";
        
        boolean kondisi = tkode.getText().equals("") || tnama.getText().equals("");
        if ( kondisi ) {
            JOptionPane.showMessageDialog(null, "Data tidak boleh kosong");
        } else {
            try {
                PreparedStatement stat = conn.prepareStatement(sqlQuery);
                stat.setString(1, tnama.getText());
                stat.setString(2, tjenis.getSelectedItem().toString());
                stat.setString(3, tsatuan.getText());
                stat.setString(4, thgbeli.getText());
                stat.setString(5, thgjual.getText());
                stat.setString(6, tstok.getText());
                stat.setString(7, tkode.getText());
                
                stat.executeUpdate();
                JOptionPane.showMessageDialog(null, "Data Berhasil Diubah");
                kosong();
                tkode.requestFocus();
                datatable();
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, "Data Gagal Diubah : "+e);
            }
        }
    }//GEN-LAST:event_beditActionPerformed

