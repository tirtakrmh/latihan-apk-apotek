    private void bcariActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bcariActionPerformed
        // TODO add your handling code here:
        Object[] Baris = {"Kode ","Nama Obat","Jenis Obat","Satuan","Harga Beli","Harga Jual","Stok"};
        tabmode = new DefaultTableModel(null, Baris);
        tabelobat.setModel(tabmode);
        String sql = "select*from obat where nama like '%"+tcari.getText()+"%'";
        try{
            java.sql.Statement stat = conn.createStatement();
            ResultSet hasil = stat.executeQuery(sql);
            while(hasil.next()){
                String a = hasil.getString("kode");
                String b = hasil.getString("nama");
                String c = hasil.getString("jenis");
                String d = hasil.getString("satuan");
                String e = hasil.getString("hgbeli");
                String f = hasil.getString("hgjual");
                String g = hasil.getString("stok");
                           
                String[] data={a,b,c,d,e,f,g};
                tabmode.addRow(data);
            }
        } catch (Exception e){
        }
    }//GEN-LAST:event_bcariActionPerformed

    private void tabelobatMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tabelobatMouseClicked
        // TODO add your handling code here:
        int baris = tabelobat.getSelectedRow();
        String kode = tabmode.getValueAt(baris, 0).toString();
        String nama = tabmode.getValueAt(baris, 1).toString();
        String jenis = tabmode.getValueAt(baris, 2).toString();
        String satuan = tabmode.getValueAt(baris, 3).toString();
        String hgbeli = tabmode.getValueAt(baris, 4).toString();
        String hgjual = tabmode.getValueAt(baris, 5).toString();
        String stok = tabmode.getValueAt(baris, 6).toString();

        tkode.setText(kode);
        tnama.setText(nama);
        tjenis.setSelectedItem(jenis);
        tsatuan.setText(satuan);
        thgbeli.setText(hgbeli);
        thgjual.setText(hgjual);
        tstok.setText(stok);
    }//GEN-LAST:event_tabelobatMouseClicked

